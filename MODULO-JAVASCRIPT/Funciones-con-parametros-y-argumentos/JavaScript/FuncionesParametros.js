//Funciones con parametros y argumentos
function sumar(numer1, numero2){ //numero1 y numero2 son parametros
    console.log(numer1 + numero2);
}
sumar(10,10); //argumentos o valores reales
sumar(10,20); //argumentos o valores reales
sumar(10,30); //argumentos o valores reales
sumar(10,50); //argumentos o valores reales

const sumar2 = function(n1, n2){
    console.log(n1 + n2);
}

